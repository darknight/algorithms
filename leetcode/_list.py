#!/usr/bin/env python3


class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next


class DoubleListNode:
    def __init__(self, key=0, val=0, prev=None, next=None):
        self.key = key
        self.val = val
        self.prev = prev
        self.next = next


